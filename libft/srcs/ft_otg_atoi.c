int		ft_otg_atoi(char **cur)
{
	int ret;
	int sign;

	sign = 1;
	ret = 0;
	if (**cur == '-')
	{
		(*cur)++;
		sign = -1;
	}
	while (**cur <= '9' && **cur >= '0')
	{
		ret = 10 * ret - (**cur - '0');
		(*cur)++;
	}
	return ((sign > 0 ? -ret : ret));
}
